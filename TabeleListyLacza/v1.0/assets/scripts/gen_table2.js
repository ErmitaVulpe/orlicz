function gen_table2() {
    var output = ""
    document.getElementById("table2-content").innerHTML = output
    for (var rows = 0; rows < dict.length; rows++) {
        var output = ""
        output += `<tr><td>${rows+1}.</td>`
        for (var cells = 0; cells < dict[rows].length; cells++) {
            if (cells == 0) {
                output += `<td class="table2-area-hoverable" data-modal-target="#${backend_names[rows]}">${dict[rows][cells]}</td>`
            } else
            if (cells == 2) {
                output += "<td>" + dict[rows][cells].toString().replace(".", ",") + "</td>"
            } else if (cells == 5) {
                output += "<td><ul>"
                for (let lolo = 0; lolo < dict[rows][cells].length; lolo++) {
                    output += "<li>" + dict[rows][cells][lolo].toString().replace(".", ",") + "</li>"
                }
                output += "</ul></td>"
            } else if (cells == 8) {
                output += `<td><img src="./assets/img/manufacturers/${dict[rows][cells]}.png"></td>`
            } else {
                output += "<td>" + dict[rows][cells] + "</td>"
            }
        }
        output += "</tr>"
        document.getElementById("table2-content").innerHTML += output
    }
    popup()
}

